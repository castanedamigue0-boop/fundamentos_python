#CREAR UNA LISTA

#indice           0         1       2       3        4         5         6       
#indice           -6        -5      -4     -3        -2         -1        0
estudiantes = ["yisel", "laura", "luis", "jhon", "stiven", "ronalt", "andres"]
print(estudiantes)

#ACCEDER A UN ELEMENTO DE LA LISTA
print(estudiantes[5]) #ronalt

#MODIFICAR UN ELEMENTO DE LA LISTA
estudiantes[3]= "miguel"
print(estudiantes)

#consultar rangos de elementos de la lista 
print(estudiantes[0:5]) # ["yisel", laura]
print(estudiantes[:3]) # ["yisel", "laura", "luis"]
print(estudiantes[3:3]) #[]
print(estudiantes[0:2:4])

