# 1. Declarar lista de productos
productos = ["Cuadernos", "Lapiceros", "Borradores", "Reglas", "Marcadores", "Carpetas"]

# 2. Declarar lista de precios (floats)
precios = [2500.0, 1200.0, 500.0, 1500.0, 3000.0, 4500.0]

# 3. Declarar lista de cantidades (enteros)
cantidades = [50, 100, 80, 30, 25, 40]

# 4. Imprimir listas y cantidad de productos
print("Productos:", productos)
print("Precios:", precios)
print("Cantidades:", cantidades)
print(f"Total de productos en el inventario: {len(productos)}")

# 5. Mostrar tipos de datos y explicación
print(f"Tipo de dato de la lista: {type(productos)}")
print(f"Tipo de dato del primer elemento: {type(productos[0])}")
